export function PERMIT2_ADDRESS(chainId: SupportedChainId | undefined): string {
  if (chainId == SupportedChainId.BITLAYER_TESTNET) {
    return '0x3EA312569078c754cf907623A434589b3ac15D2A'
  }
  if (chainId == SupportedChainId.CYBER_TESTNET) {
    return '0x79E4EC7dD8D57c83bE2Ac4b1E3EF5AEaEC084B0A'
  }
  if (chainId == SupportedChainId.LINEA_SEPOLIA) {
    return '0x099272f57De9d5f0a5d1ffD728983A96B3E8a7E8'
  }
  if (chainId == SupportedChainId.LINEA) {
    return '0x1707ae46dedCBdC62ca011Bcc3baCf50853F0675'
  }
  return PERMIT2_ADDRESS_DEFAULT
}
